a=10
b=5
#additon
print(a+b)

#subtraction
print(a-b)

#multiplication
print(a*b)

#division
print(a/b)

#modulus (remainder of a/b)
print(a%b)

#exponentiation (10*10*10*10*10) (5 times of 10)
print(a**b)

#floor division
print(a//b)

#examples 
print(10+5)
15
print(10-5)
5
print(10*5)
50
print(10/5)
2.0
print(10%5)
0
print(10**5)
100000
