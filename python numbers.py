#python numbers
#integers- whole number without decimals

x = 1
y = 245678950
z = -245678
print(type(x))
print(type(y))
print(type(z))


#float- numbers contaning one or more decimals
x = 2.50
y = 2.2
z = -2.25

print(type(x))
print(type(y))
print(type(z))

#float can also be scientific numbers with 'e'
x = 25e4
y = 15E6
z = -57.8e91


#complex- written with 'j'
x = 2+4j
y = 4j
z = -4j

print(type(x))
print(type(y))
print(type(z))
