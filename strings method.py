#string
a = "hello world!!"
print(a)
print(type(a))

#index method
a = "hello world"
print(a.index("e"))
print(a.index("world"))

#find method
print(a.find("e"))

#substring
print(a[1])
print(a[2:5])
print(a[1:5])

#strip method- removes any whitespace from beggining to end
print(a.strip())

#len method- 
print(len(a))

#lower method
print(a.lower())

#upper method
print(a.upper())

#replace method
print(a.replace("h",'j'))

#split method
print(a.split())



